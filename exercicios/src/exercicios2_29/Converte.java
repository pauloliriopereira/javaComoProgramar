package exercicios2_29;

// Este aplicativo exibe os equivalentes inteiros de algumas letras maiúsculas, letras minúsculas, dígitos e 
// símbolos especiais. Exiba os equivalentes inteiros do seguinte: A B C a b c 0 1 2 3 $ * + / e o caractere
// em branco.

public class Converte
{
	// método principal inicia a execução do programa
	public static void main( String[] args )
	{
		
		System.out.printf( "O valor de 'A' %d.\n", ( int ) 'A' );
		System.out.printf( "O valor de 'B' %d.\n", ( int ) 'B' );
		System.out.printf( "O valor de 'C' %d.\n", ( int ) 'C' );
		System.out.printf( "O valor de 'a' %d.\n", ( int ) 'a' );
		System.out.printf( "O valor de 'b' %d.\n", ( int ) 'b' );
		System.out.printf( "O valor de 'c' %d.\n", ( int ) 'c' );
		System.out.printf( "O valor de '0' %d.\n", ( int ) '0' );
		System.out.printf( "O valor de '1' %d.\n", ( int ) '1' );
		System.out.printf( "O valor de '2' %d.\n", ( int ) '2' );
		System.out.printf( "O valor de '3' %d.\n", ( int ) '3' );
		System.out.printf( "O valor de '$' %d.\n", ( int ) '$' );
		System.out.printf( "O valor de '*' %d.\n", ( int ) '*' );
		System.out.printf( "O valor de '+' %d.\n", ( int ) '+' );
		System.out.printf( "O valor de '/' %d.\n", ( int ) '/' );
		System.out.printf( "O valor de ' ' %d.\n", ( int ) ' ' );

	} // fim do método main
	
} // fim da classe Converte